from __future__ import annotations
import abc
from ast import Return
from dataclasses import Field, dataclass
from pathlib import Path
from typing import Any
from pydantic import BaseModel, ValidationError
from enum import Enum
from pydantic.json_schema import model_json_schema


@dataclass
class ToolInvocation:
    
    params : dict[str, Any]
    cwd :  Path 

@dataclass
class ToolResults:
    success : bool
    output : str
    error : str | None = None
    metadata : dict[str, Any] = Field(default_factory=dict)

@dataclass
class ToolConfirmation:
    tool_name : str
    params : dict[str, Any]
        

class ToolKind(str, Enum):
    READ = "read"
    WRITE = "write"
    SHELL = "shell"
    NETWORK = "network"
    MEMORY = "memory"
    MCP = "mcp"


class Tool(abc.ABC):
    
    name  = "base_tool"
    description = "Base tool"
    kind : ToolKind = ToolKind.READ
    
    def __init__(self):
        super().__init__()
        pass
    
    @property
    def schema(self) -> dict[str, Any] | type['BaseModel']:    
        raise NotImplementedError("Tool must define schema property !")
    
    @abc.abstractmethod
    async def execute(self, invocation : ToolInvocation) -> ToolResults:
        ...
        
    
    def validate_params(self, params : dict[str, Any]):
        schema = self.schema
        if isinstance(schema, type) and issubclass(schema, BaseModel):
            try:
                schema(**params)
            except ValidationError as e:
                errors = []
                for error in e.errors():
                    field = ".".join(str(x) for x in error.get("loc",[]))
                    msg = error.get("msg","Validation error")
                    errors.append(f"Parameter '{field}' : {msg}")
                    
                return errors
            except Exception as e:
                return [str(e)]
        return []
    
    def is_mutating(self, params : dict[str, Any]): 
        return self.kind in {ToolKind.WRITE, ToolKind.SHELL, ToolKind.NETWORK, ToolKind.MEMORY}   
        
    async def get_confirmation(self, invocation : ToolInvocation) -> ToolInvocation | None:
        if not self.is_mutating(invocation.params):
            return None
        
        return ToolConfirmation(
            tool_name= self.name,
            params=invocation.params,
            description= f"Execute {self.name}"
        )
        
    def to_openai_schema(self) :
        
        schema = self.schema
        if isinstance(schema, type) and issubclass(schema, BaseModel):

            json_schema = model_json_schema(schema , mode='serialization')
            return { "name" : self.name,
                      "parameters" : {
                            "type" : "object",
                            "properties": json_schema.get('properties',{}),
                            "required" : json_schema.get('required', []),
                            
                      }}
        
        if isinstance(schema, dict):
            
            result = {"name":self.name, "description" : self.description}
            
            if 'parameters' in schema:
                result['parameters'] = schema['parameters']
            else:
                result['parameters'] = schema
            
            return result
        
        raise ValueError(f'Invalid schema type for tool {self.name}: {type(schema)}') 
    
               